package nntc.volosov.coursework;

public class Genres {
    private int id;
    private String genre;

    public Genres(int id, String genre) {
        this.id = id;
        this.genre = genre;
    }

    public int getId() {
        return id;
    }

    public String getGenre() {
        return genre;
    }

    public String getName() {

        return genre;
    }
}